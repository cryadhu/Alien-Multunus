class AlienDetails:
	codename = None
	bloodColor = None
	noAntenas = None
	noLegs = None
	home = None
	
	def getCodeName(self):
		return self.codename
	def getBloodColor(self):
		return self.bloodColor
	def getNoOfAntenas(self):
		return self.noAntenas
	def getNoOfLegs(self):
		return self.noLegs
	def getHome(self):
		return self.home
		
	def	setCodeName(self,codename):
		self.codename = codename
	def setBloodColor(self,bloodColor):
		self.bloodColor = bloodColor
	def setNoOfAntenas(self,noAntenas):
		self.noAntenas = noAntenas
	def setNoOfLegs(self,noLegs):
		self.noLegs = noLegs
	def setHome(self,home):
		self.home = home
	
